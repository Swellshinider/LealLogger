namespace LealLogger;

/// <summary>
/// Enum to define the log level.
/// </summary>
public enum LogLevel
{
	/// <summary>
	/// Debug level.
	/// </summary>
	DEBUG,
	
	/// <summary>
	/// Information level.
	/// </summary>
	INFO,
	
	/// <summary>
	/// Warning level.
	/// </summary>
	WARN,
	
	/// <summary>
	/// Error level.
	/// </summary>
	ERROR,
	
	/// <summary>
	/// Fatal level.
	/// </summary>
	FATAL
}